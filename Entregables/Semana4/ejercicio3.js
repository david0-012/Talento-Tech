// 3. Calcular el área y perímetro de un rectángulo, solicitando al usuario las medidas de sus lados.
let base = prompt("Introduce la base del triángulo");
let altura = prompt("Introduce la altura del triángulo");
let area = (base * altura) 
let perimetro = 2*(base + altura);
window.alert(`El area del rectangulo es de: ${area} con un perimetro de: ${perimetro}`);